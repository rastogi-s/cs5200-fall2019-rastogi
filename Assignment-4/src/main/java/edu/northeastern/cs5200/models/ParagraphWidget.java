package edu.northeastern.cs5200.models;

import javax.persistence.Entity;
import javax.persistence.Table;

/**
 * The Class ParagraphWidget.
 */
@Entity
@Table(name = "paragraph_widget")
public class ParagraphWidget extends Widget {

	/** The text. */
	private String text;

	/**
	 * Gets the text.
	 *
	 * @return the text
	 */
	public String getText() {
		return text;
	}

	/**
	 * Sets the text.
	 *
	 * @param text the new text
	 */
	public void setText(String text) {
		this.text = text;
	}

	/**
	 * Hash code.
	 *
	 * @return the int
	 */
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = super.hashCode();
		result = prime * result + ((text == null) ? 0 : text.hashCode());
		return result;
	}

	/**
	 * Equals.
	 *
	 * @param obj the obj
	 * @return true, if successful
	 */
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (!super.equals(obj))
			return false;
		if (getClass() != obj.getClass())
			return false;
		ParagraphWidget other = (ParagraphWidget) obj;
		if (text == null) {
			if (other.text != null)
				return false;
		} else if (!text.equals(other.text))
			return false;
		return true;
	}

	/**
	 * To string.
	 *
	 * @return the string
	 */
	@Override
	public String toString() {
		return "ParagraphWidget [text=" + text + ", getId()=" + getId() + ", getType()=" + getType() + ", getWidth()="
				+ getWidth() + ", getHeight()=" + getHeight() + "]";
	}

}
